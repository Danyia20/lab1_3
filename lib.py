def count_common_elements(*lists):
    """
    Принимает произвольное количество списков и возвращает количество элементов,
    которые присутствуют во ВСЕХ списках (пересечение).

    :param lists: произвольное число списков (например, list1, list2, ...)
    :return: int — количество общих элементов
    """
    if not lists:
        return 0

    # Преобразуем первый список в множество
    common = set(lists[0])

    # Последовательно пересекаем со всеми остальными
    for lst in lists[1:]:
        common &= set(lst)

    return len(common)